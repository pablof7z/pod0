use rusqlite::Connection;

use crate::StorageError;
use crate::schema_introspection::require_columns;

pub(crate) fn validate_transcript_workflow_schema(
    connection: &Connection,
) -> Result<(), StorageError> {
    require_columns(
        connection,
        "pod0_transcript_workflow_imports",
        &[
            "backup_byte_count",
            "backup_digest",
            "committed_at_ms",
            "row_count",
            "singleton",
            "source_fingerprint",
            "source_generation",
            "staged_at_ms",
            "state",
            "verified_at_ms",
        ],
    )?;
    require_columns(
        connection,
        "pod0_transcript_workflow_import_rows",
        &[
            "classification",
            "episode_id",
            "ordinal",
            "row_bytes",
            "row_fingerprint",
            "source_generation",
        ],
    )?;
    require_columns(
        connection,
        "pod0_transcript_workflows",
        &[
            "attempt",
            "attempt_id",
            "cancellation_id",
            "command_id",
            "committed_artifact_id",
            "committed_content_digest",
            "committed_transcript_version_id",
            "completion_artifact_id",
            "created_at_ms",
            "deadline_at_ms",
            "episode_id",
            "evidence_input_version",
            "expected_selection_revision",
            "external_operation_id",
            "failure_code",
            "failure_detail",
            "failure_retryable",
            "issued_revision",
            "local_audio_url",
            "max_attempts",
            "may_have_submitted",
            "model",
            "not_before_ms",
            "origin",
            "provider",
            "provider_fallback_enabled",
            "provider_status",
            "publisher_first",
            "publisher_mime_hint",
            "publisher_transcript_url",
            "remote_audio_url",
            "request_id",
            "resulting_selection_revision",
            "source_generation",
            "source_revision",
            "stage",
            "submission_authorized_at_ms",
            "submission_fence_id",
            "updated_at_ms",
            "workflow_id",
            "workflow_revision",
        ],
    )?;
    require_columns(
        connection,
        "pod0_transcript_attempts",
        &[
            "attempt",
            "attempt_id",
            "authorized_at_ms",
            "completion_artifact_id",
            "created_at_ms",
            "episode_id",
            "external_operation_id",
            "failure_code",
            "failure_detail",
            "may_have_submitted",
            "provider_status",
            "request_id",
            "state",
            "submission_fence_id",
            "updated_at_ms",
            "workflow_id",
        ],
    )?;
    require_columns(
        connection,
        "pod0_transcript_evidence_requests",
        &[
            "completed_at_ms",
            "content_digest",
            "episode_id",
            "input_version",
            "requested_at_ms",
            "state",
            "transcript_version_id",
            "workflow_id",
        ],
    )
}
